    private void jTableguestlistMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableguestlistMouseClicked
        int index = jTableguestlist.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTableguestlist.getModel();

        guest_name1.setText(model.getValueAt(index,1).toString());
        guest_mobile.setText(model.getValueAt(index,2).toString());
        guest_address.setText(model.getValueAt(index,7).toString());
        guest_checkIn.setText(model.getValueAt(index,9).toString());
        guest_checkOut.setText(model.getValueAt(index,10).toString());
        guest_roomNo.setText(model.getValueAt(index,8).toString());
        guest_tprice.setText(model.getValueAt(index,11).toString());
        guest_email.setText(model.getValueAt(index,5).toString());
        
        try{
            SimpleDateFormat myFormat = new SimpleDateFormat("yyyy/MM/dd");
            Calendar cal = Calendar.getInstance();
            String todaystr = myFormat.format(cal.getTime());
            java.util.Date today = myFormat.parse(todaystr);

            String checkOutDateStr = guest_checkOut.getText();
            java.util.Date checkOutDate = myFormat.parse(checkOutDateStr);

            long difference = checkOutDate.getTime()-today.getTime();
            int noOfDays = (int)(difference/(1000*60*60*24));

            if (noOfDays>=1)
            {
                jLabel68.setText("<html>Leave In<br>"+String.valueOf(noOfDays)+"<br>Days");
                jLabel68.setHorizontalAlignment(SwingConstants.CENTER);
                lable_checkouttoday.setBackground(new Color(231,248,236));
                jLabel68.setForeground(new Color(96,207,131));
            }
            else if(noOfDays==0)
            {
                jLabel68.setText("<html>They<br>leave in<br>today");
                jLabel68.setHorizontalAlignment(SwingConstants.CENTER);
                lable_checkouttoday.setBackground(new Color(255,234,245));
                jLabel68.setForeground(new Color(234,50,61));
            }}
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }

    }//GEN-LAST:event_jTableguestlistMouseClicked

